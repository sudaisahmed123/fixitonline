<script>
 function getLocation(event) {
    event.preventDefault(); // Prevent the form from submitting immediately

    if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(
            function(position) {
                document.getElementById('latitude').value = position.coords.latitude;
                document.getElementById('longitude').value = position.coords.longitude;

                console.log("Latitude: " + position.coords.latitude);
                console.log("Longitude: " + position.coords.longitude);

                // Now submit the form after setting latitude and longitude
                document.querySelector('form').submit();
            },
            function(error) {
                switch (error.code) {
                    case error.PERMISSION_DENIED:
                        alert("User denied the request for Geolocation. Please allow location access.");
                        break;
                    case error.POSITION_UNAVAILABLE:
                        alert("Location information is unavailable.");
                        break;
                    case error.TIMEOUT:
                        alert("The request to get user location timed out.");
                        break;
                    case error.UNKNOWN_ERROR:
                        alert("An unknown error occurred.");
                        break;
                }
                // Optionally allow form submission here if you want to proceed without location
                // document.querySelector('form').submit();
            }
        );
    } else {
        alert("Geolocation is not supported by this browser.");
    }
}


</script>


